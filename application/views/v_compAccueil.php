<div id="contenu">
	<h2>Gestion des frais des visiteurs</h2>
	<p>Bienvenue dans votre application de gestion des frais de déplacements des visiteurs. </p>
	<p>
		Afin de confirmer les frais des déplacements professionnels des visiteurs, vous devez valider des fiches de frais mensuelles des visiteurs.
	</p>
	<ul>
		<li>Les fiches de frais sont envoyées automatiquement pour vérification aux comptables;</li>
		<li>Vous devrez la valider pour valider la mise en paiement, ou la refuser pour la renvoyer au visiteur concerné;</li>
	</ul>

	<p>
		Au moyen du bandeau gauche, vous avez accès aux fonctionalités
		du profil comptable :
	</p>
	<ul>
		<li>Gérer les fiches de frais (valider, refuser, imprimer)</li>
		<li>Se déconnecter</li>
	</ul>
</div>
